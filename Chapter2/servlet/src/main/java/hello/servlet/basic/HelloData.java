package hello.servlet.basic;

public class HelloData {
  private String username;
  private int age;

  public void setUsername(String username) {
    this.username = username;
  }

  public void setAge(int age) {
    this.age = age;
  }

  public String getUsername() {
    return username;
  }

  public int getAge() {
    return age;
  }
}
